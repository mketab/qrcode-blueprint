qrcommon.maskFunc = {
	[-1] = function(_,_) return false end,
	[0] = function(x,y) return (y+x)%2==0 end,
	[1] = function(x,y) return y%2==0 end,
	[2] = function(x,y) return x%3==0 end,
	[3] = function(x,y) return (y+x)%3==0 end,
	[4] = function(x,y) return (y%4-1.5)*(x%6-2.5)>0 end,
	[5] = function(x,y) return (y*x)%2+(y*x)%3==0 end,
	[6] = function(x,y) return ((y*x)%3+y*x)%2==0 end,
	[7] = function(x,y) return ((y*x)%3+y+x)%2==0 end,
}

function qrcommon.get_char_count_bits(version, mode)
	local i = mode
	if mode == 4 then
		i = 3
	elseif mode == 8 then
		i = 4
	end
	if i > 4 then return 8 end
	local tab = { {10,9,8,8},{12,11,16,10},{14,13,16,12} }
	if version < 10 then
		return tab[1][i]
	elseif version < 27 then
		return tab[2][i]
	elseif version <= 40 then
		return tab[3][i]
	end
	return 8
end

